#include "../../inc/libft.h"

void	*ft_memset(void *m, char c, size_t len)
{
	t_uint8	*s;
	size_t	i;

	s = (t_uint8 *)m;
	i = 0;
	while (i < len)
		s[i++] = c;
	return (m);
}
