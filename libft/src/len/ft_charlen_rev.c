#include "../../inc/libft.h"

size_t	ft_charlen_rev(char *c)
{
	size_t	size;

	size = 1;
	if (*c < 0)
	{
		while (!(*c & 0x40) && size++ < 4)
			c--;
	}
	return (size);
}