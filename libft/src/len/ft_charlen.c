#include "../../inc/libft.h"

size_t	ft_charlen(t_uint8 c)
{
	size_t	size;

	size = 1;
	if (c & 0x80)
	{
		c <<= 1;
		while (c & 0x80 && size++ < 4)
			c <<= 1;
	}
	return (size);
}